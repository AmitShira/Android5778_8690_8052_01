package com.example.amit.android5778_8690_8052_01.model.entities;


public class Car {
    private int Branch_Number;
    private int Kilometerage;
    private int Car_Number;
    private int CodeModel;


    public int getBranch_Number() {
        return Branch_Number;
    }

    public void setBranch_Number(int branch_Number) {
        Branch_Number = branch_Number;
    }

    public int getKilometerage() {
        return Kilometerage;
    }

    public void setKilometerage(int kilometerage) {
        Kilometerage = kilometerage;
    }

    public int getCar_Number() {
        return Car_Number;
    }

    public void setCar_Number(int car_Number) {
        Car_Number = car_Number;
    }


    public int getCodeModel() {
        return CodeModel;
    }

    public void setCodeModel(int codeModel) {
        CodeModel = codeModel;
    }
}
