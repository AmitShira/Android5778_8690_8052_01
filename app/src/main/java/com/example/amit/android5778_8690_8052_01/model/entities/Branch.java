public class Branch {

    private Address Branch_Address;
    private int Number_Of_Parking_Lots;
    private int Branch_Number;

    public Address getBranch_Address() {
        return Branch_Address;
    }

    public void setBranch_Address(Address branch_Address) {
        Branch_Address = branch_Address;
    }

    public int getNumber_Of_Parking_Lots() {
        return Number_Of_Parking_Lots;
    }

    public void setNumber_Of_Parking_Lots(int number_Of_Parking_Lots) {
        Number_Of_Parking_Lots = number_Of_Parking_Lots;
    }

    public int getBranch_Number() {
        return Branch_Number;
    }

    public void setBranch_Number(int branch_Number) {
        Branch_Number = branch_Number;
    }
}
